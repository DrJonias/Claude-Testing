const CELL = 10; // 280px canvas = 28 model pixels × 10px each

// State
let classifier = null;
let p5sketch = null;
let currentWord = null;
let penSize = 1; // in grid cells (1 = 10×10px, 2 = 20×20px, 3 = 30×30px)
let timerInterval = null;
let startTime = null;
let hasDrawn = false;
let round = 1;
let won = false;
let inferInterval = null;

function toDE(en) {
  return EN_TO_DE[en.toLowerCase()] || en;
}

// p5 sketch – 280×280 canvas, drawing snaps to 10×10 grid
new p5(function(p) {
  p5sketch = p;
  let isDrawing = false;

  p.setup = function() {
    p.pixelDensity(1);
    const cnv = p.createCanvas(280, 280);
    cnv.parent('canvas-container');
    p.background(255);
    p.noStroke();
  };

  p.draw = function() {};

  function paintCell(x, y) {
    const gx = Math.floor(x / CELL) * CELL;
    const gy = Math.floor(y / CELL) * CELL;
    const s = penSize * CELL;
    p.fill(0);
    p.rect(gx, gy, s, s);
  }

  function paintLine(x1, y1, x2, y2) {
    const dx = x2 - x1, dy = y2 - y1;
    const steps = Math.max(1, Math.ceil(Math.sqrt(dx*dx + dy*dy) / (CELL / 2)));
    for (let i = 0; i <= steps; i++) {
      const t = i / steps;
      paintCell(x1 + dx * t, y1 + dy * t);
    }
  }

  p.mousePressed = function() {
    if (!isInCanvas()) return;
    isDrawing = true;
    document.getElementById('canvas-container').classList.add('drawing');
    if (!hasDrawn && !won) { hasDrawn = true; startTimer(); }
    paintCell(p.mouseX, p.mouseY);
  };

  p.mouseDragged = function() {
    if (!isDrawing || won) return;
    paintLine(p.pmouseX, p.pmouseY, p.mouseX, p.mouseY);
  };

  p.mouseReleased = function() {
    isDrawing = false;
    document.getElementById('canvas-container').classList.remove('drawing');
  };

  p.touchStarted = function() {
    if (!isInCanvas()) return false;
    isDrawing = true;
    document.getElementById('canvas-container').classList.add('drawing');
    if (!hasDrawn && !won) { hasDrawn = true; startTimer(); }
    const pos = getTouchPos();
    paintCell(pos.x, pos.y);
    return false;
  };
  p.touchMoved = function() {
    if (!isDrawing || won) return false;
    const pos = getTouchPos();
    paintLine(p.pmouseX, p.pmouseY, pos.x, pos.y);
    return false;
  };
  p.touchEnded = function() {
    isDrawing = false;
    document.getElementById('canvas-container').classList.remove('drawing');
    return false;
  };

  function getTouchPos() {
    const t = p.touches[0];
    const r = p.canvas.getBoundingClientRect();
    return {
      x: (t.clientX - r.left) * (p.width / r.width),
      y: (t.clientY - r.top)  * (p.height / r.height)
    };
  }

  function isInCanvas() {
    return p.mouseX >= 0 && p.mouseX <= p.width && p.mouseY >= 0 && p.mouseY <= p.height;
  }
});

// Scales 280×280 canvas down to 28×28 for the model (exact 10:1, no interpolation artifacts)
function getClassifyCanvas() {
  const tmp = document.createElement('canvas');
  tmp.width = 28; tmp.height = 28;
  const ctx = tmp.getContext('2d');
  ctx.imageSmoothingEnabled = false;
  ctx.drawImage(p5sketch.canvas, 0, 0, 28, 28);
  return tmp;
}

// ml5 0.12.2 async DoodleNet loading
async function initClassifier() {
  try {
    classifier = await ml5.imageClassifier('DoodleNet');
    document.getElementById('statusDot').className = 'status-dot ready';
    document.getElementById('statusText').textContent = 'KI bereit!';
    document.getElementById('startBtn').disabled = false;
  } catch (e) {
    document.getElementById('statusText').textContent = 'Ladefehler – Seite neu laden';
    console.error('DoodleNet load error:', e);
  }
}


// Game
function startGame() {
  document.getElementById('startScreen').classList.add('hidden');
  nextRound();
}

function nextRound() {
  won = false;
  hasDrawn = false;
  stopTimer();
  clearInterval(inferInterval);
  document.getElementById('winOverlay').classList.add('hidden');
  document.getElementById('canvas-container').classList.remove('win');

  const en = PLAYABLE[Math.floor(Math.random() * PLAYABLE.length)];
  currentWord = { en, de: toDE(en) };
  document.getElementById('wordDE').textContent = currentWord.de;
  document.getElementById('wordEN').textContent = '(' + currentWord.en + ')';
  document.getElementById('roundNum').textContent = round++;
  document.getElementById('timer').textContent = '0.0s';
  document.getElementById('timer').className = 'timer';
  document.getElementById('topGuess').textContent = '–';
  document.getElementById('predList').innerHTML =
    '<div style="color:var(--muted);font-size:.85rem">Fange an zu zeichnen…</div>';

  if (p5sketch) p5sketch.background(255);

  inferInterval = setInterval(runClassify, 500);
}

window.skipWord = function() { nextRound(); };

window.clearDrawing = function() {
  if (p5sketch) p5sketch.background(255);
  hasDrawn = false;
  stopTimer();
  document.getElementById('timer').textContent = '0.0s';
  document.getElementById('timer').className = 'timer';
  document.getElementById('predList').innerHTML =
    '<div style="color:var(--muted);font-size:.85rem">Fange an zu zeichnen…</div>';
  document.getElementById('topGuess').textContent = '–';
};

window.setPen = function(el) {
  penSize = parseInt(el.dataset.size);
  document.querySelectorAll('.pen-btn').forEach(b => b.classList.remove('active'));
  el.classList.add('active');
};

// Timer
function startTimer() {
  startTime = Date.now();
  timerInterval = setInterval(() => {
    const s = (Date.now() - startTime) / 1000;
    const el = document.getElementById('timer');
    el.textContent = s.toFixed(1) + 's';
    el.className = 'timer ' + (s < 10 ? 'fast' : s < 20 ? '' : 'slow');
  }, 100);
}
function stopTimer() {
  clearInterval(timerInterval);
  timerInterval = null;
}

// Classification
function runClassify() {
  if (!classifier || !hasDrawn || won || !p5sketch) return;
  classifier.classify(getClassifyCanvas()).then(results => {
    if (!results || won) return;
    updatePredUI(results.slice(0, 5));
    const match = results.slice(0, 10).find(r => r.label.toLowerCase() === currentWord.en.toLowerCase());
    if (match && match.confidence > 0.08) {
      triggerWin();
    }
  }).catch(() => {});
}

function updatePredUI(results) {
  const target = currentWord.en.toLowerCase();
  const list = document.getElementById('predList');
  list.innerHTML = '';
  results.forEach(r => {
    const name = r.label;
    const de = toDE(name);
    const pct = Math.round(r.confidence * 100);
    const isMatch = name.toLowerCase() === target;
    const item = document.createElement('div');
    item.className = 'pred-item';
    item.innerHTML = `
      <div class="pred-top">
        <span class="pred-name ${isMatch ? 'match' : ''}">${de}</span>
        <span class="pred-pct">${pct}%</span>
      </div>
      <div class="pred-bar-wrap">
        <div class="pred-bar ${isMatch ? 'match' : ''}" style="width:${Math.min(pct,100)}%"></div>
      </div>`;
    list.appendChild(item);
  });
  if (results[0]) {
    document.getElementById('topGuess').textContent = toDE(results[0].label);
  }
}

function triggerWin() {
  if (won) return;
  won = true;
  stopTimer();
  clearInterval(inferInterval);
  document.getElementById('canvas-container').classList.add('win');
  const elapsed = startTime ? ((Date.now() - startTime) / 1000).toFixed(1) : '?';
  document.getElementById('winSub').textContent = `Die KI hat "${currentWord.de}" erkannt!`;
  document.getElementById('winTime').textContent = elapsed;
  document.getElementById('winOverlay').classList.remove('hidden');
  launchConfetti();
}

// Confetti
function launchConfetti() {
  const cc = document.getElementById('confettiCanvas');
  cc.width = window.innerWidth; cc.height = window.innerHeight;
  const cctx = cc.getContext('2d');
  const colors = ['#7c6aff','#ff6ab0','#3dffa0','#ffb347','#4fc3f7'];
  const pieces = Array.from({length:130}, () => ({
    x: Math.random() * cc.width,
    y: -10 - Math.random() * 300,
    r: 4 + Math.random() * 7,
    color: colors[Math.floor(Math.random()*colors.length)],
    vx: (Math.random()-.5)*5,
    vy: 2 + Math.random()*5,
    angle: Math.random()*360,
    spin: (Math.random()-.5)*7
  }));
  let frame = 0;
  function animate() {
    cctx.clearRect(0, 0, cc.width, cc.height);
    pieces.forEach(p => {
      p.x += p.vx; p.y += p.vy; p.angle += p.spin;
      cctx.save();
      cctx.translate(p.x, p.y);
      cctx.rotate(p.angle * Math.PI/180);
      cctx.fillStyle = p.color;
      cctx.fillRect(-p.r/2, -p.r/2, p.r, p.r);
      cctx.restore();
    });
    frame++;
    if (frame < 150) requestAnimationFrame(animate);
    else cctx.clearRect(0, 0, cc.width, cc.height);
  }
  animate();
}

window.startGame = startGame;
